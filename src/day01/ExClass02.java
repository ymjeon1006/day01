package day01;

public class ExClass02 {
	public static void main(String[] args) {
		System.out.println("안\n녕\n하\n세\n요");
		System.out.println("1\t1234567\t12345678\t1");
		System.out.println("이름\t나이");
		System.out.println("홍길동\t20살");
		System.out.println("   \"   \\   ");
		
		System.out.println(100 + 100);
		System.out.println(100.111 + 100.222);
		
		System.out.println("안녕" + "하세요");
		System.out.println("백" + 100);
		System.out.println("100" + "200");
		
		System.out.println("안녕" + 100 + 200);
		System.out.println("안녕" + (100 + 200));
	}
}